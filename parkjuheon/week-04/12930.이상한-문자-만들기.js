// 문제: 이상한 문자 만들기
// 레벨: Level 1
// 문제 간략 설명: 짝수번째 알파벳은 대문자, 홀수번째 알파벳은 소문자로 바꿔서 반환하는 문제
/*
풀이:
 1. 문자열을 공백 기준으로 나눔
 2. 각 단어의 문자들을 순회하며 인덱스가 짝수면 대문자, 홀수면 소문자로 변환
 3. 변환된 문자들을 다시 합쳐 단어를 만들고, 단어들을 공백으로 합쳐 최종 문자열 반환
*/

function solution(s) {
  return s
    .split(" ")
    .map((word) =>
      word
        .split("")
        .map((c, i) => (i % 2 === 0 ? c.toUpperCase() : c.toLowerCase()))
        .join(""),
    )
    .join(" ");
}
